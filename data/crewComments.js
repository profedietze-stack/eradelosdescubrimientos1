export function seleccionarComentario(gameState) {
  const r = Math.random();
  const comments = [];

  if (r < 0.35) {
    const statsComments = seleccionarPorStats(gameState);
    if (statsComments) comments.push(...statsComments);
  }
  if (r >= 0.35 && r < 0.65) {
    const locComments = seleccionarPorUbicacion(gameState);
    if (locComments) comments.push(...locComments);
  }
  if (r >= 0.65 || comments.length === 0) {
    comments.push(...CREW_COMMENTS.random);
  }

  const flat = comments.flat();
  return flat.length > 0 ? flat[Math.floor(Math.random() * flat.length)] : null;
}

function seleccionarPorStats(gameState) {
  const grupos = [];
  if (gameState.moral >= 75) grupos.push(...CREW_COMMENTS.moral_alta);
  else if (gameState.moral <= 35) grupos.push(...CREW_COMMENTS.moral_baja);
  if (gameState.suministros >= 75) grupos.push(...CREW_COMMENTS.suministros_altos);
  else if (gameState.suministros <= 35) grupos.push(...CREW_COMMENTS.suministros_bajos);
  if (gameState.integridad >= 75) grupos.push(...CREW_COMMENTS.integridad_alta);
  else if (gameState.integridad <= 35) grupos.push(...CREW_COMMENTS.integridad_baja);
  if (gameState.autoridad >= 75) grupos.push(...CREW_COMMENTS.autoridad_alta);
  else if (gameState.autoridad <= 35) grupos.push(...CREW_COMMENTS.autoridad_baja);
  return grupos.length > 0 ? [grupos[Math.floor(Math.random() * grupos.length)]] : null;
}

function seleccionarPorUbicacion(gameState) {
  const progress = gameState.progress || 0;
  let key = 'alta_mar';
  if (progress <= 1) key = 'salida';
  else if (progress <= 4) key = 'canarias';
  else if (progress >= 5 && progress <= 8) key = 'sargazos';
  else if (progress >= 28 && progress <= 30) key = 'tierra_vista';
  else if (progress >= 31 && progress <= 36) key = 'la_espanola';
  else if (progress >= 37) key = 'regreso';
  const pool = CREW_COMMENTS.location[key];
  return pool ? [pool[Math.floor(Math.random() * pool.length)]] : null;
}
